package com.lannstark.lec01

fun main() {
    // (1) var(variable): 가변(바꿀 수 있는 변수)
    var number1 = 10L // var number1: Long = 10L

    // (2) val(value): 불변(바꿀 수 없는 변수)
    val number2 = 10L // val number2: Long = 20L

    // (3) reference type
    var number3 = 1_000L

    // (4) 객체 인스턴스화
    var person = Person("Kotlin")
}